TEST(Neighbors, Level2) {
    auto defaultConfig = Z7::Z7Configuration{};
    int failures = 0;
    int testCount = 1;

    auto testData = GetParsedTestData(neighborTestDataLevel2, std::size(neighborTestDataLevel2));

    for (auto &[cellIndex, expected_neighbors]: testData) {
        const auto neighbors = Z7::neighbors(cellIndex, defaultConfig);
        for (auto &expected_neighbor: expected_neighbors) {
            auto it = std::find(neighbors.begin(), neighbors.end(), expected_neighbor);
            if (it == neighbors.end()) {
                EXPECT_TRUE(false) << "Cell: " << cellIndex.str()
                                   << "\n\tReported neighbors: " << JoinIndexes(neighbors).str()
                                   << "\n\tExpected neighbors: " << JoinIndexes(expected_neighbors).str();
                ++failures;
                break;
            }
        }
        ++testCount;
    }
    EXPECT_EQ(failures, 0) << "There were " << failures << " failures in " << testCount << " tests.";
}

