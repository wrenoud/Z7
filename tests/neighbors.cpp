struct ParsedNeighborTestData {
    Z7::Z7Index cell;
    std::vector<Z7::Z7Index> expected_neighbors;
};

std::vector<ParsedNeighborTestData> GetParsedTestData(const char *data[], size_t dataSize) {
    std::vector<ParsedNeighborTestData> parsedData;

    for (size_t i = 0; i < dataSize; ++i) {
        // We split the line by spaces, the first entry is the cell index, the rest are the expected neighbors.
        std::istringstream lineStream(data[i]);

        std::string cellStr;
        lineStream >> cellStr;

        Z7::Z7Index cellIndex(cellStr);

        std::vector<Z7::Z7Index> expected_neighbors;
        while (!lineStream.eof()) {
            lineStream >> cellStr;
            expected_neighbors.emplace_back(cellStr);
        }

        parsedData.push_back({cellIndex, std::move(expected_neighbors)});
    }
    return parsedData;
}

// Helper function to join a container of Z7Index into a string space separated string.
template <typename T>
std::ostringstream JoinIndexes(const T &indexes)
{
    std::ostringstream indexesStream;
    for (const auto &n: indexes) {
        indexesStream << n.str() << " ";
    }
    return indexesStream;
}

